import { existsSync, readFileSync, writeFileSync } from 'node:fs';
import { join } from 'node:path';

const repoRoot = process.cwd();
const reportPath = join(repoRoot, 'mango-docs/evidence/2026-05-30-frontend-productization-correction/sprint-4/package-export-report.json');

const requiredExports = [
  ['mango-ui/packages/admin/package.json', '.', { types: './src/index.d.ts', import: './dist/index.js' }],
  ['mango-ui/packages/admin/package.json', './style.css', './style.css'],
  ['mango-ui/packages/admin-shell/package.json', '.', { types: './src/index.ts', import: './dist/index.js' }],
  ['mango-ui/packages/admin-shell/package.json', './style.css', './style.css'],
  ['mango-ui/packages/admin-shell/package.json', './home', './src/views/home/index.vue'],
  ['mango-ui/packages/admin-shell/package.json', './dev-pages', './src/views/demo/registerDevPages.ts'],
  ['mango-ui/packages/auth/package.json', '.', { types: './src/index.d.ts', import: './dist/index.js' }],
  ['mango-ui/packages/auth/package.json', './style.css', './dist/style.css'],
  ['mango-ui/packages/common/package.json', '.', { types: './index.ts', import: './dist/index.js' }],
  ['mango-ui/packages/common/package.json', './theme/index.css', './theme/index.css'],
  ['mango-ui/packages/admin-pages/package.json', '.', { types: './src/index.ts', import: './dist/index.js' }],
  ['mango-ui/packages/admin-pages/package.json', './core', './src/core.ts'],
  ['mango-ui/packages/admin-pages/package.json', './defaults', './src/defaults.ts'],
  ['mango-ui/packages/notice/package.json', '.', { types: './src/index.ts', import: './dist/index.js' }],
  ['mango-ui/packages/notice/package.json', './admin', { types: './src/admin.ts', import: './dist/admin.js' }],
  ['mango-ui/packages/notice/package.json', './client', { types: './src/client.ts', import: './dist/client.js' }],
];

const requiredFiles = [
  'mango-ui/packages/admin/src/index.ts',
  'mango-ui/packages/admin/src/index.d.ts',
  'mango-ui/packages/admin/admin-packages.json',
  'mango-ui/packages/admin/generated-package-styles.css',
  'mango-ui/packages/admin/style.css',
  'mango-ui/scripts/generate-package-styles.mjs',
  'mango-ui/scripts/generate-admin-package-styles.mjs',
  'mango-ui/packages/admin-shell/style.css',
  'mango-ui/packages/common/theme/index.css',
];

const appForbiddenPatterns = [
  {
    file: 'mango-ui/packages/create-mango-app/templates/mango-business-starter/frontend/apps/{{projectKebab}}-admin/src/main.ts',
    forbidden: ['@mango/admin-shell', '@mango/common/theme', 'element-plus/dist/index.css'],
  },
];

const errors = [];
const details = [];

function readJson(relativePath) {
  return JSON.parse(readFileSync(join(repoRoot, relativePath), 'utf8'));
}

function check(condition, message, detail) {
  details.push({ ok: Boolean(condition), message, detail });
  if (!condition) {
    errors.push(message);
  }
}

function sameJsonValue(left, right) {
  return JSON.stringify(left) === JSON.stringify(right);
}

for (const [packagePath, exportName, expectedTarget] of requiredExports) {
  const pkg = readJson(packagePath);
  const actual = pkg.exports?.[exportName];
  check(
    sameJsonValue(actual, expectedTarget),
    `${packagePath} export ${exportName} must be ${JSON.stringify(expectedTarget)}`,
    { actual },
  );
}

for (const relativePath of requiredFiles) {
  check(existsSync(join(repoRoot, relativePath)), `${relativePath} must exist`);
}

for (const item of appForbiddenPatterns) {
  const content = readFileSync(join(repoRoot, item.file), 'utf8');
  for (const forbidden of item.forbidden) {
    check(!content.includes(forbidden), `${item.file} must not import ${forbidden}`);
  }
  check(content.includes("import '@mango/admin/style.css'"), `${item.file} must import @mango/admin/style.css`);
  check(content.includes("from '@mango/admin'"), `${item.file} must import createMangoAdminApp from @mango/admin`);
}

const styleContent = readFileSync(join(repoRoot, 'mango-ui/packages/admin/style.css'), 'utf8');
const generatedStyleContent = readFileSync(join(repoRoot, 'mango-ui/packages/admin/generated-package-styles.css'), 'utf8');
const shellStyleContent = readFileSync(join(repoRoot, 'mango-ui/packages/admin-shell/style.css'), 'utf8');
check(styleContent.includes("@import 'element-plus/dist/index.css'"), '@mango/admin/style.css must include Element Plus base style');
check(styleContent.includes("@import './generated-package-styles.css'"), '@mango/admin/style.css must include generated package style entry');
check(generatedStyleContent.includes('scripts/generate-package-styles.mjs'), '@mango/admin generated package styles must be generated by generic style tool');
check(generatedStyleContent.includes("@import '@mango/admin-shell/style.css'"), '@mango/admin generated package styles must include admin-shell style entry');
check(generatedStyleContent.includes("@import '@mango/auth/style.css'"), '@mango/admin generated package styles must include auth style entry');
check(!styleContent.includes('dist/style.css'), '@mango/admin/style.css must not depend on ignored dist output');
check(!styleContent.includes('.scss'), '@mango/admin/style.css must not require consumer Sass support');
check(!generatedStyleContent.includes('.scss'), '@mango/admin generated package styles must not require consumer Sass support');
check(shellStyleContent.includes("@import './dist/style.css'"), '@mango/admin-shell/style.css must expose built shell component styles');
check(!shellStyleContent.includes('.scss'), '@mango/admin-shell/style.css must not require consumer Sass support');

const report = {
  ok: errors.length === 0,
  checkedAt: new Date().toISOString(),
  errors,
  details,
};

writeFileSync(reportPath, JSON.stringify(report, null, 2));

if (errors.length > 0) {
  console.error(`Package export contract failed. Report: ${reportPath}`);
  for (const error of errors) {
    console.error(`- ${error}`);
  }
  process.exit(1);
}

console.log(`Package export contract passed. Report: ${reportPath}`);
