#!/usr/bin/env node
import { existsSync, readFileSync, writeFileSync } from 'node:fs';
import { join, relative } from 'node:path';
import { fileURLToPath } from 'node:url';

const rootDir = fileURLToPath(new URL('..', import.meta.url));
const adminDir = join(rootDir, 'packages/admin');
const manifestPath = join(adminDir, 'admin-packages.json');
const outputPath = join(adminDir, 'generated-package-styles.css');
const adminPackagePath = join(adminDir, 'package.json');
const checkOnly = process.argv.includes('--check');

function readJson(path) {
  return JSON.parse(readFileSync(path, 'utf8'));
}

function fail(message) {
  console.error(`[admin-package-styles] ${message}`);
  process.exit(1);
}

function resolvePackageDir(packageName) {
  if (!packageName.startsWith('@mango/')) {
    fail(`Only @mango packages can be declared: ${packageName}`);
  }
  return join(rootDir, 'packages', packageName.slice('@mango/'.length));
}

function validateManifest(manifest, adminPackageJson) {
  if (manifest.schemaVersion !== 1) {
    fail(`Unsupported schemaVersion: ${manifest.schemaVersion}`);
  }
  if (!Array.isArray(manifest.packages) || manifest.packages.length === 0) {
    fail('admin-packages.json must declare at least one package.');
  }

  const seen = new Set();
  for (const item of manifest.packages) {
    if (!item || typeof item.name !== 'string' || typeof item.style !== 'string') {
      fail('Each package item must include string name and style fields.');
    }
    if (seen.has(item.name)) {
      fail(`Duplicate admin package declaration: ${item.name}`);
    }
    seen.add(item.name);

    if (item.style !== `${item.name}/style.css`) {
      fail(`${item.name} style must use its public style export: ${item.name}/style.css`);
    }
    if (!adminPackageJson.dependencies?.[item.name]) {
      fail(`@mango/admin package.json is missing dependency: ${item.name}`);
    }

    const packageDir = resolvePackageDir(item.name);
    const packageJsonPath = join(packageDir, 'package.json');
    if (!existsSync(packageJsonPath)) {
      fail(`Package directory does not contain package.json: ${relative(rootDir, packageJsonPath)}`);
    }
    const packageJson = readJson(packageJsonPath);
    if (packageJson.name !== item.name) {
      fail(`Package name mismatch in ${relative(rootDir, packageJsonPath)}: ${packageJson.name}`);
    }
    if (!packageJson.exports?.['./style.css']) {
      fail(`${item.name} must export ./style.css before admin can aggregate it.`);
    }
  }
}

function renderStyles(manifest) {
  const imports = manifest.packages.map((item) => `@import '${item.style}';`).join('\n');
  return `/* This file is generated by scripts/generate-admin-package-styles.mjs. */\n/* Edit packages/admin/admin-packages.json, then run pnpm -F @mango/admin generate:styles. */\n${imports}\n`;
}

const manifest = readJson(manifestPath);
const adminPackageJson = readJson(adminPackagePath);
validateManifest(manifest, adminPackageJson);

const nextContent = renderStyles(manifest);
const currentContent = existsSync(outputPath) ? readFileSync(outputPath, 'utf8') : '';

if (checkOnly) {
  if (currentContent !== nextContent) {
    fail(`${relative(rootDir, outputPath)} is out of date. Run pnpm -F @mango/admin generate:styles.`);
  }
  console.log('[admin-package-styles] generated-package-styles.css is up to date.');
  process.exit(0);
}

writeFileSync(outputPath, nextContent);
console.log(`[admin-package-styles] wrote ${relative(rootDir, outputPath)}`);
