package com.mango.plugin.perm;

import org.apache.maven.plugin.AbstractMojo;
import org.apache.maven.plugin.MojoExecutionException;
import org.apache.maven.plugins.annotations.LifecyclePhase;
import org.apache.maven.plugins.annotations.Mojo;
import org.apache.maven.plugins.annotations.Parameter;

import java.io.IOException;
import java.nio.file.Files;
import java.nio.file.Path;
import java.nio.file.Paths;

/**
 * 生成权限 SQL
 * mvn mango:gen-permission -Dmodule=sys -Dresource=user
 */
@Mojo(name = "gen-permission", defaultPhase = LifecyclePhase.GENERATE_SOURCES)
public class GenPermissionMojo extends AbstractMojo {

    @Parameter(property = "module", required = true)
    private String module;

    @Parameter(property = "resource", required = true)
    private String resource;

    @Parameter(property = "outputDir", defaultValue = "${project.basedir}/src/main/resources")
    private String outputDir;

    @Override
    public void execute() throws MojoExecutionException {
        getLog().info("Generating permission SQL for " + module + ":" + resource);

        try {
            String sql = generatePermissionSql();
            Path outputPath = Paths.get(outputDir, "permission_" + module + "_" + resource + ".sql");
            Files.writeString(outputPath, sql);
            getLog().info("Permission SQL generated: " + outputPath);
        } catch (IOException e) {
            throw new MojoExecutionException("Failed to generate permission SQL", e);
        }
    }

    private String generatePermissionSql() {
        String code = module + ":" + resource;
        String path = "/" + module.replace("sys-", "").replace("sys/", "") + "/" + resource;

        return """
-- ============================================
-- 权限 SQL - %s
-- Generated by Mango Scaffold
-- ============================================

-- 菜单
INSERT INTO sys_menu (name, code, path, component, type, sort)
VALUES ('%s管理', '%s', '%s', '%s/index', 1, 1);

-- 按钮权限
INSERT INTO sys_menu (name, code, perms, parent_id, type, sort)
SELECT '查看', '%s:view', '%s:view',
       (SELECT id FROM sys_menu WHERE code = '%s'), 2, 1;

INSERT INTO sys_menu (name, code, perms, parent_id, type, sort)
SELECT '新增', '%s:add', '%s:add',
       (SELECT id FROM sys_menu WHERE code = '%s'), 2, 2;

INSERT INTO sys_menu (name, code, perms, parent_id, type, sort)
SELECT '修改', '%s:edit', '%s:edit',
       (SELECT id FROM sys_menu WHERE code = '%s'), 2, 3;

INSERT INTO sys_menu (name, code, perms, parent_id, type, sort)
SELECT '删除', '%s:delete', '%s:delete',
       (SELECT id FROM sys_menu WHERE code = '%s'), 2, 4;

-- API 权限
INSERT INTO sys_permission (name, code, path, method)
VALUES ('查询%s', '%s:list', '%s', 'GET');

INSERT INTO sys_permission (name, code, path, method)
VALUES ('查看%s', '%s:view', '%s/{id}', 'GET');

INSERT INTO sys_permission (name, code, path, method)
VALUES ('新增%s', '%s:add', '%s', 'POST');

INSERT INTO sys_permission (name, code, path, method)
VALUES ('修改%s', '%s:edit', '%s/{id}', 'PUT');

INSERT INTO sys_permission (name, code, path, method)
VALUES ('删除%s', '%s:delete', '%s/{id}', 'DELETE');
""".formatted(
                resource,
                resource, code, path, resource,
                code, code, code,
                code, code, code,
                code, code, code,
                code, code, code,
                resource, code, path,
                resource, code, path,
                resource, code, path,
                resource, code, path,
                resource, code, path
        );
    }
}
