package com.mango.plugin.gen;

import org.apache.maven.plugin.AbstractMojo;
import org.apache.maven.plugin.MojoExecutionException;
import org.apache.maven.plugins.annotations.LifecyclePhase;
import org.apache.maven.plugins.annotations.Mojo;
import org.apache.maven.plugins.annotations.Parameter;

import java.io.IOException;
import java.nio.file.Files;
import java.nio.file.Path;
import java.nio.file.Paths;

/**
 * 生成模块脚手架
 * mvn mango:gen-module -Dname=user
 */
@Mojo(name = "gen-module", defaultPhase = LifecyclePhase.GENERATE_SOURCES)
public class GenModuleMojo extends AbstractMojo {

    @Parameter(property = "name", required = true)
    private String name;

    @Parameter(property = "baseDir", defaultValue = "${project.basedir}")
    private String baseDir;

    @Override
    public void execute() throws MojoExecutionException {
        getLog().info("Generating module: " + name);

        try {
            Path modulePath = Paths.get(baseDir, "mango-" + name);
            createModuleStructure(modulePath);
            getLog().info("Module generated: " + modulePath);
        } catch (IOException e) {
            throw new MojoExecutionException("Failed to generate module", e);
        }
    }

    private void createModuleStructure(Path basePath) throws IOException {
        // 创建模块目录
        Files.createDirectories(basePath);

        // 4个子模块
        String[] subModules = {"api", "core", "starter", "starter-remote"};
        for (String subModule : subModules) {
            Path subPath = basePath.resolve("mango-" + name + "-" + subModule);
            Files.createDirectories(subPath);

            // 创建 src 结构
            Files.createDirectories(subPath.resolve("src/main/java"));
            Files.createDirectories(subPath.resolve("src/test/java"));

            // 创建 pom.xml
            String pomContent = generatePomXml(subModule);
            Files.writeString(subPath.resolve("pom.xml"), pomContent);
        }

        // 生成 README
        String readme = "# " + name + " 模块\n\nGenerated by Mango Scaffold\n";
        Files.writeString(basePath.resolve("README.md"), readme);
    }

    private String generatePomXml(String subModule) {
        return String.format("""
<?xml version="1.0" encoding="UTF-8"?>
<project xmlns="http://maven.apache.org/POM/4.0.0"
         xmlns:xsi="http://www.w3.org/2001/XMLSchema-instance"
         xsi:schemaLocation="http://maven.apache.org/POM/4.0.0 http://maven.apache.org/xsd/maven-4.0.0.xsd">
    <modelVersion>4.0.0</modelVersion>

    <parent>
        <groupId>com.mango</groupId>
        <artifactId>mango-%s</artifactId>
        <version>1.0.0-SNAPSHOT</version>
    </parent>

    <artifactId>mango-%s-%s</artifactId>
    <packaging>jar</packaging>

</project>
""", name, name, subModule);
    }
}
